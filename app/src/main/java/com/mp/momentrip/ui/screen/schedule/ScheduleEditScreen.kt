@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ScheduleCreationScreen(
    userViewModel: UserViewModel,
    recommendViewModel: RecommendViewModel,
    onScheduleCreated: (Schedule) -> Unit
) {
    val primaryBlue  = MaterialTheme.colorScheme.primary
    val outlineGray  = Color(0xFFE5E9F2)
    val surfaceWhite = Color.White

    var title      by remember { mutableStateOf("") }
    var region     by remember { mutableStateOf("") }
    var startDate  by remember { mutableStateOf<LocalDate?>(null) }
    var endDate    by remember { mutableStateOf<LocalDate?>(null) }
    var error      by remember { mutableStateOf<String?>(null) }
    var isCreating by remember { mutableStateOf(false) }
    var isGenerating by remember { mutableStateOf(false) }

    val calendarState = rememberUseCaseState()
    val boundary = LocalDate.now().minusYears(2)..LocalDate.now().plusYears(2)
    val defaultRange = LocalDate.now().plusDays(1)..LocalDate.now().plusDays(4)

    val autoSchedule by recommendViewModel.autoGeneratedSchedule.collectAsState()

    // â³ ìƒì„± ì™„ë£Œ ì‹œ ì½œë°± ì‹¤í–‰
    LaunchedEffect(autoSchedule) {
        if (isGenerating && autoSchedule != null) {
            isGenerating = false
            onScheduleCreated(autoSchedule!!)
        }
    }

    // â³ ë¡œë”© ì¤‘ í™”ë©´
    if (isGenerating) {
        AiLoadingScreen()
        return
    }

    Column(
        modifier = Modifier
            .fillMaxSize()
            .padding(horizontal = 24.dp)
            .verticalScroll(rememberScrollState())
    ) {
        Spacer(Modifier.height(24.dp))

        Text(
            text = "ìƒˆë¡œìš´ ì—¬í–‰",
            style = MaterialTheme.typography.headlineSmall,
            modifier = Modifier.fillMaxWidth(),
            textAlign = TextAlign.Center
        )

        Spacer(Modifier.height(32.dp))

        /* ì œëª© */
        Label("ì—¬í–‰ ì´ë¦„")
        OutlinedTextField(
            value = title,
            onValueChange = { title = it },
            modifier = Modifier
                .fillMaxWidth()
                .border(1.dp, outlineGray, RoundedCornerShape(16.dp))
                .background(surfaceWhite, RoundedCornerShape(16.dp)),
            shape = RoundedCornerShape(16.dp),
            colors = OutlinedTextFieldDefaults.colors(
                focusedBorderColor = primaryBlue,
                unfocusedBorderColor = Color.Transparent,
                focusedContainerColor = surfaceWhite,
                unfocusedContainerColor = surfaceWhite,
            ),
            textStyle = MaterialTheme.typography.bodyLarge
        )

        Spacer(Modifier.height(28.dp))

        /* ë‚ ì§œ */
        Label("ë‚ ì§œ")
        DateSelectionCard(startDate, endDate, outlineGray, surfaceWhite) {
            calendarState.show()
        }

        Spacer(Modifier.height(28.dp))

        /* ì§€ì—­ */
        Label("ì—¬í–‰ ì§€ì—­")
        RegionSelectorCard(
            selectedRegion = region,
            onRegionSelected = { region = it },
            outlineGray = outlineGray,
            primaryBlue = primaryBlue,
            surfaceWhite = surfaceWhite
        )

        Spacer(Modifier.height(32.dp))

        /* ì—ëŸ¬ ë©”ì‹œì§€ */
        error?.let {
            Text(text = it, color = Color.Red, style = MaterialTheme.typography.bodyMedium)
            Spacer(Modifier.height(8.dp))
        }
        Row(
            modifier = Modifier.fillMaxWidth(),
            horizontalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            // ì¼ë°˜ ì¼ì • ë§Œë“¤ê¸°
            Button(
                onClick = {
                    if (region.isNotEmpty() && startDate != null && endDate != null) {
                        isCreating = true
                        userViewModel.createSchedule(
                            title = title,
                            region = region,
                            startDate = startDate!!,
                            endDate = endDate!!,
                            onSuccess = {
                                isCreating = false
                                onScheduleCreated(autoSchedule!!)
                            },
                            onError = { msg -> isCreating = false; error = msg }
                        )
                    } else {
                        error = "ì—¬í–‰ ë‚ ì§œì™€ ì§€ì—­ì„ ëª¨ë‘ ì„ íƒí•˜ì„¸ìš”."
                    }
                },
                enabled = !isCreating,
                modifier = Modifier
                    .weight(1f)
                    .height(52.dp),
                shape = RoundedCornerShape(14.dp)
            ) {
                Text("ì¶”ê°€", style = MaterialTheme.typography.titleSmall)
            }

            // AI ì¼ì • ìƒì„±
            OutlinedButton(
                onClick = {
                    if (region.isNotEmpty() && startDate != null && endDate != null) {
                        isGenerating = true
                        recommendViewModel.generateAutoSchedule(
                            userViewModel = userViewModel,
                            region = region,
                            startDate = startDate!!,
                            endDate = endDate!!,
                            onSuccess = { onScheduleCreated(it) }
                        )
                    } else {
                        error = "ì—¬í–‰ ë‚ ì§œì™€ ì§€ì—­ì„ ëª¨ë‘ ì„ íƒí•˜ì„¸ìš”."
                    }
                },
                enabled = !isCreating && !isGenerating,
                modifier = Modifier
                    .weight(1f)
                    .height(52.dp),
                shape = RoundedCornerShape(14.dp),
                border = BorderStroke(1.dp, primaryBlue),
                colors = ButtonDefaults.outlinedButtonColors(contentColor = primaryBlue)
            ) {
                Text("ðŸ¤– AI ì¼ì • ìƒì„±", style = MaterialTheme.typography.titleSmall)
            }
        }


        Spacer(Modifier.height(48.dp))
    }

    CalendarDialog(
        state = calendarState,
        config = CalendarConfig(
            yearSelection = true,
            monthSelection = true,
            boundary = boundary,
            style = CalendarStyle.MONTH
        ),
        selection = CalendarSelection.Period(
            selectedRange = defaultRange.toRange()
        ) { s, e -> startDate = s; endDate = e }
    )
}

@Composable
private fun Label(text: String) =
    Text(
        text = text,
        color = Color(0xFF8E8E93),
        style = MaterialTheme.typography.labelLarge,
        modifier = Modifier.padding(bottom = 8.dp)
    )

@Composable
private fun DateSelectionCard(
    startDate: LocalDate?,
    endDate: LocalDate?,
    outlineGray: Color,
    surfaceWhite: Color,
    onClick: () -> Unit
) {
    val formatter = DateTimeFormatter.ofPattern("yyyy.MM.dd")
    val label = if (startDate != null && endDate != null)
        "${startDate.format(formatter)}  â€“  ${endDate.format(formatter)}"
    else "ë‚ ì§œë¥¼ ì„ íƒí•˜ì„¸ìš”"

    Box(
        modifier = Modifier
            .fillMaxWidth()
            .border(1.dp, outlineGray, RoundedCornerShape(16.dp))
            .background(surfaceWhite, RoundedCornerShape(16.dp))
            .clickable { onClick() }
            .padding(20.dp)
    ) {
        Text(
            text = label,
            style = MaterialTheme.typography.bodyLarge,
            color = if (startDate != null) MaterialTheme.colorScheme.onSurface else Color.Gray
        )
    }
}

@Composable
private fun RegionSelectorCard(
    selectedRegion: String,
    onRegionSelected: (String) -> Unit,
    outlineGray: Color,
    primaryBlue: Color,
    surfaceWhite: Color
) {
    val regions = listOf(
        "ì„œìš¸", "ì¸ì²œ", "ëŒ€ì „", "ëŒ€êµ¬", "ê´‘ì£¼", "ë¶€ì‚°", "ìš¸ì‚°", "ì„¸ì¢…",
        "ê²½ê¸°", "ê°•ì›", "ì¶©ë¶", "ì¶©ë‚¨", "ì „ë¶", "ì „ë‚¨", "ê²½ë¶", "ê²½ë‚¨", "ì œì£¼"
    )

    FlowRow(
        modifier = Modifier
            .fillMaxWidth()
            .padding(12.dp),
        horizontalArrangement = Arrangement.spacedBy(12.dp),
        verticalArrangement = Arrangement.spacedBy(12.dp),
        maxItemsInEachRow = 3
    ) {
        regions.forEach { region ->
            val selected = region == selectedRegion

            Box(
                modifier = Modifier
                    .fillMaxWidth(0.3f) // ì¹´ë“œ ë„ˆë¹„ ë¹„ìœ¨
                    .height(48.dp)
                    .clip(RoundedCornerShape(16.dp))
                    .background(
                        if (selected) primaryBlue else surfaceWhite
                    )
                    .border(
                        width = if (selected) 0.dp else 1.dp,
                        color = if (selected) Color.Transparent else outlineGray,
                        shape = RoundedCornerShape(16.dp)
                    )
                    .clickable { onRegionSelected(region) },
                contentAlignment = Alignment.Center
            ) {
                Text(
                    text = region,
                    color = if (selected) Color.White else Color(0xFF1B1E28),
                    fontWeight = if (selected) FontWeight.Bold else FontWeight.Medium,
                    fontSize = 14.sp
                )
            }
        }
    }
}

